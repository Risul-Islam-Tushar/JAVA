package JavaCodes;

public class ArithmeticOperation {
public static void main (String[] args) {
	int num1,num2,result;
	
	num1 = 34;
	num2 = 12;
	
	result = num1 + num2;
	System.out.println("Sum =" +result);
	
	result = num1 - num2;
	System.out.println("Sub =" +result);
	
	result = num1 * num2;
	System.out.println("Mul =" +result);
}
}
